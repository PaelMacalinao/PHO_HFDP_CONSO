// Escape HTML helper function
function escapeHtml(text) {
    if (!text) return '';
    const div = document.createElement('div');
    div.textContent = text;
    return div.innerHTML;
}

// Generate Target dropdown options
function generateTargetOptions(selectedValue) {
    let html = '<option value="">Select Target</option>';
    TARGET_OPTIONS.forEach(option => {
        const selected = (selectedValue && selectedValue === option) ? 'selected' : '';
        html += `<option value="${escapeHtml(option)}" ${selected}>${escapeHtml(option)}</option>`;
    });
    return html;
}
